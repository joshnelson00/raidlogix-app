document.addEventListener('DOMContentLoaded', function() {
    const probabilityInput = document.getElementById("id_probability");  // Ensure to match the id generated by Django forms
    const impactInput = document.getElementById('id_impact');           // Ensure to match the id generated by Django forms
    const scoreInput = document.getElementById('id_score');             // Ensure to match the id generated by Django forms

    function calculateScore() {
        const probability = parseFloat(probabilityInput.value) / 100 || 0;
        const impact = parseFloat(impactInput.value) || 0;
        const score = probability * impact;
        scoreInput.value = score.toFixed(2); // Format to 2 decimal places
    }

    probabilityInput.addEventListener('input', calculateScore);
    impactInput.addEventListener('input', calculateScore);
});